num1 = int(input("숫자1 ==> "))
num2 = int(input("숫자2 ==> "))
sum = num1 + num2
minus = num1 - num2
multi = num1 * num2
division = num1 / num2
rest = num1 % num2
square = num1 ** num2
print(num1, " + ", num2, " = ", sum)
print(num1, " - ", num2, " = ", minus)
print(num1, " * ", num2, " = ", multi)
print(num1, " / ", num2, " = ", division)
print(num1, " % ", num2, " = ", rest)
print(num1, " ** ", num2, " = ", square)
